int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration----------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USB_DEVICE_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();

  /* USER CODE BEGIN 2 */
  timer_start();
  start_rx_uart();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t last_pps_capture = pps_capture.irq_milli;
  uint32_t lastFlush = 0;
  while (1) {
    static char int_print_buffer[64];
    char *p;
  /* USER CODE END WHILE */

  /* USER CODE BEGIN 3 */
    while(last_pps_capture == pps_capture.irq_milli) { // wait for a capture
      if(((HAL_GetTick() - lastFlush) > 10) && ((HAL_GetTick() - last_uartData) > 10)) {
        flush_uart();
        lastFlush = HAL_GetTick();
      }
    }

    while((HAL_GetTick() - pps_capture.irq_milli) < 10) {} // wait 10ms, then clear DCD

    pps_capture.sent_time = serial_last_cmd_ts.ack_time;
    pps_capture.sent_milli = serial_last_cmd_ts.ack_millis;

    strcpy(int_print_buffer, "$INT,"); // 4
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.irq_time, p, 10); // 4+10=14

    strcat(int_print_buffer, ","); // 14+1=15
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.cap_tim3, p, 10); // 15+5=20

    strcat(int_print_buffer, ","); // 20+1=21
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.sent_time, p, 10); // 21+10=31

    strcat(int_print_buffer, ","); // 31+1=32
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.ch2_irq_time, p, 10); // 32+5=37

    strcat(int_print_buffer, ","); // 37+1=38
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.ch2_cap_tim3, p, 10); // 38+10=48

    strcat(int_print_buffer, ","); // 48+1=49
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(pps_capture.ch2_events, p, 16); // 49+2=51
    strupr(p); // utoa is lowercase, NMEA is upper case

    uint8_t checksum = 0;
    for(uint8_t i = 0; i < strlen(int_print_buffer); i++) {
      if(int_print_buffer[i] != '$') {
        checksum = checksum ^ int_print_buffer[i];
      }
    }

    strcat(p, "*"); // 51+1=52
    p = int_print_buffer + strlen(int_print_buffer);
    utoa(checksum, p, 16); // 52+2=54
    strupr(p); // utoa is lowercase, checksum should be upper case

    strcat(p, "\r\n"); // 54+2=56

    CDC_Transmit_serial_state(0);
    last_pps_capture = pps_capture.irq_milli;

    while((HAL_GetTick() - last_uartData) < 25) {} // wait for uart to be free for 25ms
    if(flush_uart()) {
      lastFlush = HAL_GetTick();
      HAL_Delay(10);
    }
    CDC_Transmit_FS((uint8_t *)int_print_buffer, strlen(int_print_buffer));
  }
  /* USER CODE END 3 */

}

